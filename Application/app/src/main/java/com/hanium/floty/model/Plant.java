package com.hanium.floty.model;

public class Plant {

    String plantkor;
    String planteng;
    String temp;
    String hum;
    String imgurl;
    String detail;
    String water;
    String waterDetail;

    public String getPlantkor() {
        return plantkor;
    }

    public void setPlantkor(String plantkor) {
        this.plantkor = plantkor;
    }

    public String getPlanteng() {
        return planteng;
    }

    public void setPlanteng(String planteng) {
        this.planteng = planteng;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getHum() {
        return hum;
    }

    public void setHum(String hum) {
        this.hum = hum;
    }

    public String getImgurl() {
        return imgurl;
    }

    public void setImgurl(String imgurl) {
        this.imgurl = imgurl;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getWater() {
        return water;
    }

    public void setWater(String water) {
        this.water = water;
    }

    public String getWaterDetail() {
        return waterDetail;
    }

    public void setWaterDetail(String waterDetail) {
        this.waterDetail = waterDetail;
    }

}